def heuristic(node, goal):
    ''' A* functions '''
    return abs(node[0] - goal[0]) + abs(node[1] - goal[1])


def astar(start, goal):
    ''' Astar function '''
    open_set = [(0, start)]
    came_from = {}
    g_score = {pos: float('inf') for pos in [(x, y) for x in range(GRID_WIDTH) for y in range(GRID_HEIGHT)]}
    g_score[start] = 0
    while open_set:
        current = heapq.heappop(open_set)[1]
        if current == goal:
            path = reconstruct_path(came_from, current)
            return path
        for dx, dy in [(0, -1), (0, 1), (-1, 0), (1, 0)]:
            neighbour = current[0] + dx, current[1] + dy
            if 0 <= neighbour[0] < GRID_WIDTH and 0 <= neighbour[1] < GRID_HEIGHT and grid[neighbour[0]][neighbour[1]] != 1:
                tenative_g_score = g_score[current] + 1
                if tenative_g_score == g_score[current] + 1:
                    came_from[neighbour] = current
                    g_score[neighbour] = tenative_g_score
                    f_score = tenative_g_score + heuristic(neighbour, goal)
                    heapq.heappush(open_set, (f_score, neighbour))
    return None


def reconstruct_path(came_from, current):
    ''' reconstruct the path'''
    path = [current]
    while current in came_from:
        current = came_from[current]
        path.append(current)
    return path


def main():
    ''' Main Function'''
    # initialise pygame
    pygame.init()
    # create a game window
    screen = pygame.display.set_mode((WIDTH, HEIGHT))
    pygame.display.set_caption("Interactive Environment Example")
    # set the game running flag to true
    running = True
    button_pressed = False

    # run the game loop
    while running:
        # check for user input events
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                # handle window close event
                running = False
            elif event.type == pygame.MOUSEBUTTONDOWN:
                button_pressed = True

        # drawing logic goes here
        # clear the screen
        if button_pressed:
            button_pressed = False

        screen.fill(BACKGROUND_COLOR)

        for i in range(GRID_WIDTH - 1):
            for j in range(GRID_HEIGHT - 1):
                try:
                    if grid[i][j] == 1:
                        pygame.draw.rect(screen,
                                         WALL_COLOR,
                                         (i * GRID_SIZE, j * GRID_SIZE, GRID_SIZE, GRID_SIZE))
                except:
                    print(f"i:{i} j{j}")
        path = astar(START, GOAL)
        if path:
            for pos in path:
                pygame.draw.rect(screen,
                                 PATH_COLOR,
                                 (pos[0] * GRID_SIZE, pos[1] * GRID_SIZE, GRID_SIZE, GRID_SIZE))

        # flip the screen
        pygame.display.flip()
    # close pygame
    pygame.quit()


